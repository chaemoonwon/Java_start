package variable;

public class Var2 {

    public static void main(String[] args) {
        int a;  //변수 선언
        a = 20; //10 -> 20 변수 초기화

        System.out.println("a = " + a);
        System.out.println("a = " + a);
        System.out.println("a = " + a);

        /*
        * 변수 선언 : 변수를 만드는 것
        * 숫자 정수( integer )를 보관할 수 있는 이름이 a 라는 데이터 저장소를 만든다. 이것을 변수라 한다
        *
        * */

        /*
        * 변수 초기화 : 선언한 변수에 처음으로 값을 대입해서 저장하는 것
        * 자바에서 = 은 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻
        *
        * */
    }
}
