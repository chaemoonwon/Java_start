package operator;

public class Operator4 {

    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3;
        int sum4 = (2 * 2) + (3 * 3);

        // 결과는 같다
        // 괄호를 명식적으로 사용하는 것이 가독성이 좋음
        System.out.println("sum3 = " + sum3);
        System.out.println("sum4 = " + sum4);


        // 실무에서 우선순위를 외우지 않음




    }

}
