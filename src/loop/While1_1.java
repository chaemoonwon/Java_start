package loop;

public class While1_1 {

    public static void main(String[] args) {
        int count = 0;

        // 반복문
        // 같은 코드를 반복해야 하는 상황에서 반복문을 이용할 수 있음.
        // while문, for문이 있음
        count = count +1;
        System.out.println("현재 숫자는:" + count);
        count = count +1;
        System.out.println("현재 숫자는:" + count);
        count = count +1;
        System.out.println("현재 숫자는:" + count);
    }
}
