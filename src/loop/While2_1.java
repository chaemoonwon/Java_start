package loop;

public class While2_1 {
    public static void main(String[] args) {
        int sum = 0;
        int i = 10;


        sum = sum + i;  //0 + 10
        System.out.println("i= " + 1 +", sum= " + sum);
        i++;

        sum = sum + i;  //10 + 11
        System.out.println("i= " + 2 +", sum= " + sum);
        i++;

        sum = sum + i;  //21 + 12
        System.out.println("i= " + 3 +", sum= " + sum);

    }

}
